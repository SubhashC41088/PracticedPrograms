package pp2;

public class Loopassmt3 {
	//printing the even numbers between 200 to 500
	
		public static void main(String[] args) {
			int i=200;
			while( i<=500) {
				System.out.println(i+"");

				i=i+2;
			}
			System.out.println("the printed numbers are even numbers from the range of 200 to 500");
		}
		

}
